using System.Collections.Generic;

namespace TicketBookingSystem.Application;

/// <summary>
/// Generic paginated result wrapper used across all paginated queries.
/// Provides consistent pagination metadata alongside the data items.
/// </summary>
public class PagedResult<T>
{
    public List<T> Items { get; set; } = new();
    public int TotalCount { get; set; }
    public int Page { get; set; }
    public int PageSize { get; set; }
    public int TotalPages => PageSize > 0 ? (int)System.Math.Ceiling((double)TotalCount / PageSize) : 0;
    public bool HasNextPage => Page < TotalPages;
    public bool HasPreviousPage => Page > 1;
}
